package Model;

import java.util.Date;

/**
 *
 * @author Admin
 */
public class Thu {
    private String maThu;
    private String msHangMucThu;
    private String tenHangMucThu;
    private int soTien;
    private Date ngayThu;
    private String taiKhoan;
    private String MSVI;
    private String tenVi;

    
    
    
    
    
    public String getMaThu() {
        return maThu;
    }

    public void setMaThu(String maThu) {
        this.maThu = maThu;
    }

    public String getMsHangMucThu() {
        return msHangMucThu;
    }

    public void setMsHangMucThu(String msHangMucThu) {
        this.msHangMucThu = msHangMucThu;
    }

    public int getSoTien() {
        return soTien;
    }

    public void setSoTien(int soTien) {
        this.soTien = soTien;
    }

    public Date getNgayThu() {
        return ngayThu;
    }

    public void setNgayThu(Date ngayThu) {
        this.ngayThu = ngayThu;
    }

    public String getTaiKhoan() {
        return taiKhoan;
    }

    public void setTaiKhoan(String taiKhoan) {
        this.taiKhoan = taiKhoan;
    }

    

    public String getMSVI() {
        return MSVI;
    }

    public void setMSVI(String MSVI) {
        this.MSVI = MSVI;
    }

    public String getTenHangMucThu() {
        return tenHangMucThu;
    }

    public void setTenHangMucThu(String tenHangMucThu) {
        this.tenHangMucThu = tenHangMucThu;
    }

    public String getTenVi() {
        return tenVi;
    }

    public void setTenVi(String tenVi) {
        this.tenVi = tenVi;
    }
    
    
}
